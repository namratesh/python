"""Write a program to identify if the character
is an alphabet or not"""
def identy(char):
    if(char==0 or char==1 or char==2 or char==3 or char==4
       or char==5 or char==6 or char==7 or char==8 or char==9):
        print("not a alphanumeric")
    else:
        print("alphanumeric")

